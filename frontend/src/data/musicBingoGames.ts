/**
 * Pre-built Music Bingo games — 75 songs each, no duplicate artists per game
 * (except artist-themed games e.g. Taylor Swift, Jimmy Buffett).
 * Used when host selects "Use a pre-built game" for Music Bingo.
 */
import type { Song } from '../types/game';

export interface MusicBingoGame {
  id: string;
  title: string;
  description: string;
  /** Exactly 75 songs. One artist per song (unless theme allows repeats). */
  songs: Song[];
}

// Dedupe by artist (first occurrence wins), then take exactly 75
function uniqueArtists75(songs: Song[]): Song[] {
  const seen = new Set<string>();
  const out: Song[] = [];
  for (const s of songs) {
    const key = s.artist.toLowerCase();
    if (seen.has(key)) continue;
    seen.add(key);
    out.push(s);
    if (out.length === 75) break;
  }
  return out;
}

